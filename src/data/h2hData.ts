// Helper function to get H2H record between two players
export const getH2HRecord = (player1: string, player2: string): { wins: number; losses: number } | null => {
  // Check if player1 is listed as fighter
  const record1 = h2hRecords.find(r => r.fighter === player1 && r.opponent === player2);
  if (record1) {
    return { wins: record1.wins, losses: record1.losses };
  }
  
  // Check reverse (player1 is opponent)
  const record2 = h2hRecords.find(r => r.fighter === player2 && r.opponent === player1);
  if (record2) {
    return { wins: record2.losses, losses: record2.wins };
  }
  
  return null;
};
